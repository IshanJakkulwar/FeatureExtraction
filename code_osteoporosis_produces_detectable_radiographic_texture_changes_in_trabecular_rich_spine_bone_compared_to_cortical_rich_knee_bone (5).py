def plot_ellipse(x, y, ax, color, label):
    cov = np.cov(x, y)
    if cov.shape != (2, 2):
        return

    vals, vecs = np.linalg.eigh(cov)
    order = vals.argsort()[::-1]
    vals, vecs = vals[order], vecs[:, order]

    theta = np.degrees(np.arctan2(*vecs[:,0][::-1]))
    width, height = 2 * np.sqrt(vals)

    ellipse = Ellipse(
        (np.mean(x), np.mean(y)),
        width=width,
        height=height,
        angle=theta,
        edgecolor=color,
        facecolor='none',   # 🔥 outline only (fixes misleading shading)
        linestyle='--',
        linewidth=2,
        label=label
    )
    ax.add_patch(ellipse)

