def cohens_d(x, y):
    nx = len(x)
    ny = len(y)
    dof = nx + ny - 2

    pooled_std = np.sqrt(
        ((nx - 1) * np.var(x, ddof=1) + (ny - 1) * np.var(y, ddof=1)) / dof
    )

    return (np.mean(x) - np.mean(y)) / pooled_std


